String? getToday(String Today) {
  switch (Today) {
    case 'Monday':
      {
        return 'Pazartesi';
      }

    case 'Tuesday':
      {
        return 'Salı';
      }

    case 'Wednesday':
      {
        return 'Çarşamba';
      }

    case 'Thursday':
      {
        return 'Perşembe';
      }

    case 'Friday':
      {
        return 'Cuma';
      }

    case 'Saturday':
      {
        return 'Cumartesi';
      }

    case 'Sunday':
      {
        return 'Pazar';
      }
  }
}
